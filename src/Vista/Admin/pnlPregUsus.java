    private void btnInformacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInformacionActionPerformed
        FrmInfoPreguntra pregunta;
        String id;
        if (tbl.getSelectedRow() == -1) {
            id = JOptionPane.showInputDialog("Ingrese el id de la pregunta que desea ver la informacion");
            if (id != null) {
                try {

                    if (id.equalsIgnoreCase("")) {
                        JOptionPane.showMessageDialog(this, "Por favor llene el campo");
                    }
                    if (controladorPreg.validarPreguntar(id)) {
                        pregunta = new FrmInfoPreguntra(id);
                        pregunta.setVisible(true);
                        pregunta.setLocationRelativeTo(null);

                    } else {
                        JOptionPane.showMessageDialog(this, "El id ingresado no existe");
                    }
                } catch (NullPointerException | NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "No ha ingresado un numero de pregunta valida");
                }
            }
        } else {
            pregunta = new FrmInfoPreguntra(tbl.getValueAt(tbl.getSelectedRow(), 0) + "");
            pregunta.setVisible(true);
            pregunta.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_btnInformacionActionPerformed


